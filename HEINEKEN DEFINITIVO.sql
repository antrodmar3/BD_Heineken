-- 2 crear tabla  de un PRODUCTOPALET con prodcuto cuyo tipo marca sea 'CRUZCAMPO'
SELECT * FROM PRODUCTOPALET INNER JOIN PRODUCTO ON PRODUCTO.OID_PRODUCTO= PRODUCTOPALET.OID_PRODUCTO WHERE PRODUCTO.TIPOMARCA='CRUZCAMPO';

---- 3 crear una tabla conjunta en la que aparezcan los atributos TIPOMARCA, TIPOFORMATO,PRECIO,OID_PRODUCTO
---- siendo el precio del palet 120€
SELECT P.TIPOMARCA, P.TIPOFORMATO,P.PRECIO,PR.OID_PRODUCTO
FROM PRODUCTO P JOIN PRODUCTOPALET PR ON P.OID_PRODUCTO = PR.OID_PRODUCTO
WHERE P.PRECIO='120';

----4. crear una tabla en la que aparezcan los camiones de CAMIONESPUNTOVENTA que vayan a los PUNTOSDEVENTA SUPERSOL
SELECT C.OID_INTER,C.OID_PUNTODEVENTA,C.MATRICULA,PV.TELEFONO,PV.DIRECCION,PV.CADENA
FROM CAMIONESPUNTODEVENTA C JOIN PUNTODEVENTA PV ON C.OID_PUNTODEVENTA = PV.OID_PUNTODEVENTA
WHERE PV.CADENA='SUPERSOL';

----5. CUENTA EL NUMERO DE VECES QUE APARECE UNA MATRICULA----
SELECT MATRICULA,COUNT(*) AS CONTADOR1 FROM CAMIONESPEDIDO GROUP BY MATRICULA ORDER BY 1;

----6. CUENTA EL NUMERO DE VECES QUE APARECE CADA TIPO DE DESCUENTO----
SELECT TIPODESCUENTO,COUNT(*) AS CONTADOR2 FROM PEDIDO GROUP BY TIPODESCUENTO ORDER BY 1;



