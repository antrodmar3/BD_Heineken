-- 1 PROCEDIMIENTO CON CURSOR Y GROUP BY: NUMERO DE PEDIDOS QUE TRANSPORTA CADA CAMION
SET SERVEROUTPUT ON;
CREATE OR REPLACE PROCEDURE NUMERO_PEDIDOS_CAMION IS
BEGIN
DECLARE
CONTADOR1 NUMBER;
CURSOR C1 IS SELECT MATRICULA,COUNT(*) AS CONTADOR1 FROM CAMIONESPEDIDO GROUP BY MATRICULA ORDER BY 1;
REGISTRO1 C1%ROWTYPE; 
BEGIN
OPEN C1;
LOOP
  FETCH C1 INTO REGISTRO1;
  EXIT WHEN C1%NOTFOUND;
  DBMS_OUTPUT.PUT_LINE('EL NUMERO DE PEDIDOS QUE TRANSPORTA EL CAMION '||REGISTRO1.MATRICULA||' SON: '||REGISTRO1.CONTADOR1);
  END LOOP;
CLOSE C1;
END;
END;

/

