----1 CUENTA LOS NUMEROS DE PEDIDOS QUE LLEVA UN CAMION AL DIA PASANDO LA MATRICULA DEL CAMION Y LA FECHA DEL PEDIDO-------
SET SERVEROUTPUT ON
CREATE OR REPLACE FUNCTION NUMPEDIDOSALDIA(
W_MATRICULA IN CAMIONESPEDIDO.MATRICULA%TYPE,
W_FECHA IN VARCHAR2)
RETURN NUMBER IS NUMEROPEDIDOS NUMBER;
BEGIN
SELECT COUNT(*) INTO NUMEROPEDIDOS FROM PEDIDO, CAMIONESPEDIDO
WHERE CAMIONESPEDIDO.OID_PEDIDO=PEDIDO.OID_PEDIDO
AND CAMIONESPEDIDO.MATRICULA=W_MATRICULA AND TO_CHAR(PEDIDO.FECHAPEDIDO, 'DD-MM-YYYY')=W_FECHA;
RETURN NUMEROPEDIDOS;
END NUMPEDIDOSALDIA;
/

------ 2 CALCULAR LA MEDIA DE LOS PRECIOS DEL PRODUCTO QUE TIENEN TIPO FORMATO LITRONA-----
SET SERVEROUTPUT ON
CREATE OR REPLACE FUNCTION MEDIAPRECIOS(
W_PRECIO IN PRODUCTO.PRECIO%TYPE,
W_TIPOFORMATO IN PRODUCTO.TIPOFORMATO%TYPE)
RETURN NUMBER IS DEVUELVEMEDIA NUMBER;
BEGIN
SELECT AVG(PRECIO) INTO DEVUELVEMEDIA FROM PRODUCTO WHERE TIPOFORMATO='LITRONA';
RETURN DEVUELVEMEDIA;
END MEDIAPRECIOS;
/
----FUNCION 3 CON JOIN. DEVUELVE EL PRECIO DEL PALET-----
create or replace FUNCTION PRECIOPALET(
W_OID_PALET IN PALET.OID_PALET%TYPE)

RETURN NUMBER IS PREC NUMBER;
BEGIN
SELECT SUM(PRECIO) INTO PREC FROM PRODUCTO JOIN PRODUCTOPALET ON PRODUCTOPALET.OID_PRODUCTO = PRODUCTO.OID_PRODUCTO AND PRODUCTOPALET.OID_PALET=W_OID_PALET;

RETURN PREC;
END PRECIOPALET;
/


-----FUNCION 4 CON JOIN: CALCULAR CUANTOS PALET LLEVA CADA PEDIDO------
CREATE OR REPLACE FUNCTION NUMPALETSPORPEDIDO (
W_OID_PEDIDO IN PEDIDO.OID_PEDIDO%TYPE)

RETURN NUMBER IS CONT NUMBER;
BEGIN

SELECT COUNT(*) INTO CONT FROM PALET JOIN PALETPEDIDO ON PALETPEDIDO.OID_PALET = PALET.OID_PALET AND PALETPEDIDO.OID_PEDIDO=W_OID_PEDIDO;


RETURN CONT;
END NUMPALETSPORPEDIDO;
