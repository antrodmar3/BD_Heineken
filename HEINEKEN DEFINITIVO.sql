--CREACIÓN DE TABLAS

CREATE TABLE PERSONAL(
DNI VARCHAR2(20)NOT NULL,
NOMBRE VARCHAR2(20)NOT NULL,
APELLIDOS VARCHAR2(20) NOT NULL,
EMAIL VARCHAR2(30),
TELEFONO NUMBER,
PRIMARY KEY(DNI));


CREATE TABLE PRODUCTO(
OID_PRODUCTO NUMBER NOT NULL,
TIPOMARCA VARCHAR2(20),
TIPOFORMATO VARCHAR2(20),
PRECIO NUMBER,
CONSTRAINT   CHECKMARCA CHECK (TIPOMARCA='HEINEKEN' OR TIPOMARCA='CRUZCAMPO' OR TIPOMARCA='DESPERADOS' OR TIPOMARCA='AMSTEL'),
CONSTRAINT CHECKFORMATO CHECK(TIPOFORMATO='LATA' OR TIPOFORMATO='BOTELLIN 1/5' OR TIPOFORMATO='BOTELLIN 1/3' OR TIPOFORMATO='LITRONA'),
PRIMARY KEY(OID_PRODUCTO));

CREATE TABLE PALET(
OID_PALET NUMBER NOT NULL,
PRIMARY KEY(OID_PALET));

CREATE TABLE PROMOCION(
OID_PROMOCION NUMBER,
TIPOPROMO VARCHAR2(20), 
FECHAINICIOP DATE,
FECHAFINP DATE, 
CONSTRAINT CHECKPROMO CHECK (TIPOPROMO='CRUZADA' OR TIPOPROMO='VALORAÑADIDO' OR TIPOPROMO='OFERTA' OR TIPOPROMO='NULA'), 
PRIMARY KEY(OID_PROMOCION));

CREATE TABLE PEDIDO(
OID_PEDIDO NUMBER NOT NULL,
OID_PROMOCION NUMBER,
FECHAPEDIDO DATE,
FECHAENTREGA DATE,
TIPODESCUENTO VARCHAR2(20),
ESTADO VARCHAR2(20),
CONSTRAINT CHECKDESCUENTO CHECK( TIPODESCUENTO='CAMION LLENO' OR TIPODESCUENTO='PRONTO PAGO' OR TIPODESCUENTO='NULL'),
PRIMARY KEY(OID_PEDIDO),
FOREIGN KEY(OID_PROMOCION) REFERENCES PROMOCION,
CHECK(ESTADO='ENTREGADO'  OR ESTADO='SOLICITADO' OR ESTADO='CANCELADO'));

CREATE TABLE CAMION(
MATRICULA VARCHAR2(20) NOT NULL,
DNI VARCHAR2(20),
PRIMARY KEY(MATRICULA),
FOREIGN KEY(DNI) REFERENCES PERSONAL );

CREATE TABLE PUNTODEVENTA(
OID_PUNTODEVENTA NUMBER,
TELEFONO VARCHAR2(20),
DIRECCION VARCHAR2(30),
CADENA VARCHAR2(20),
PRIMARY KEY(OID_PUNTODEVENTA));

CREATE TABLE CAMIONESPUNTODEVENTA(
OID_INTER NUMBER,
OID_PUNTODEVENTA NUMBER,
MATRICULA VARCHAR2(20),
PRIMARY KEY(OID_INTER),
FOREIGN KEY(OID_PUNTODEVENTA)REFERENCES PUNTODEVENTA,
FOREIGN KEY(MATRICULA)REFERENCES CAMION);

CREATE TABLE CAMIONESPEDIDO(
OID_INTER2 NUMBER,
MATRICULA VARCHAR2(20),
OID_PEDIDO NUMBER,
PRIMARY KEY(OID_INTER2), 
FOREIGN KEY (MATRICULA) REFERENCES CAMION,
FOREIGN KEY (OID_PEDIDO) REFERENCES PEDIDO);

CREATE TABLE PRODUCTOPALET(
OID_INTER3 NUMBER,
OID_PALET NUMBER,
OID_PRODUCTO NUMBER,
PRIMARY KEY(OID_INTER3),
FOREIGN KEY (OID_PALET) REFERENCES PALET,
FOREIGN KEY (OID_PRODUCTO) REFERENCES PRODUCTO);

CREATE TABLE PALETPEDIDO(
OID_INTER4 NUMBER,
OID_PEDIDO NUMBER,
OID_PALET NUMBER,
PRIMARY KEY (OID_INTER4),
FOREIGN KEY (OID_PEDIDO) REFERENCES PEDIDO,
FOREIGN KEY (OID_PALET) REFERENCES PALET);

