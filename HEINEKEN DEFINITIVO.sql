--CREACIÓN DE TRIGGERS

--1- UN CAMIÓN NO PUEDE LLEVAR MÁS DE 5 PEDIDOS EN 1 DÍA

CREATE OR REPLACE TRIGGER MAX_PEDIDO_DIA 
BEFORE INSERT ON CAMIONESPEDIDO FOR EACH ROW 
DECLARE FEC DATE;
NUN INTEGER;
BEGIN
SELECT FECHAENTREGA INTO FEC FROM PEDIDO WHERE OID_PEDIDO= :NEW.OID_PEDIDO;
SELECT COUNT(OID_PEDIDO) INTO NUN FROM PEDIDO NATURAL JOIN CAMIONESPEDIDO WHERE FEC=FECHAENTREGA AND :NEW.MATRICULA=MATRICULA;
  IF NUN>=5
  THEN 
  raise_application_error(-20600,'Se ha superado el número máximo de pedidos');
  END IF;
END;
/


--2.- UN PALET NO PUEDE LLEVAR MÁS DE 10 PRODUCTOS

CREATE OR REPLACE TRIGGER MAXIMO_PRODUCTO_PALET 
BEFORE INSERT ON PRODUCTOPALET FOR EACH ROW
DECLARE NUN INTEGER;
BEGIN

SELECT COUNT(OID_PRODUCTO) INTO NUN FROM PRODUCTO NATURAL JOIN PRODUCTOPALET WHERE :NEW.OID_PALET=OID_PALET;
  IF NUN>=10
  THEN 
  raise_application_error(-20600,'Se ha superado el número máximo de productos');
  END IF;
END;
/

--3.- UN PEDIDO NO PUEDE CANCELARSE SI HA PASADO UN DÍA TRAS SU SOLICITUD

